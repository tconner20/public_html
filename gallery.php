    <script>
        document.addEventListener('DOMContentLoaded', function() {
            const checkboxes = document.querySelectorAll('#filter input[type="checkbox"]');
            const items = document.querySelectorAll('.gallery-grid-item');
            const noResultsMessage = document.getElementById('noResultsMessage');

            checkboxes.forEach(checkbox => {
                checkbox.addEventListener('change', filterItems);
            });

            function filterItems() {
                const selectedCategories = Array.from(checkboxes).filter(checkbox => checkbox.checked).map(checkbox => checkbox.value);
                let visibleCount = 0;

                items.forEach(item => {
                    const itemCategories = item.getAttribute('data-category').split(' ');
                    const showItem = selectedCategories.every(category => itemCategories.includes(category));

                    if (selectedCategories.length === 0 || showItem) {
                        item.style.display = '';
                        visibleCount++;
                    } else {
                        item.style.display = 'none';
                    }
                });

                noResultsMessage.style.display = visibleCount ? 'none' : 'flex';
            }
        });
    </script>

